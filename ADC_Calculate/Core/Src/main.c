float Calculate_ADC(uint16_t Adc)
{
	int i;
	uint8_t LID=ADC_Sample_Size,HID=0;
	
	float DAdc,DV,M,A,B,Out;
	
	for(i=0;i<ADC_Sample_Size;i++)
	{
		if(ADC_Sample[i].ADC<=Adc)LID=i;
	}
	if(LID<ADC_Sample_Size-1)HID=LID+1;
	else
	{
		HID=LID;
		LID--;
	}
	
	if(Adc==ADC_Sample[HID].ADC)return ADC_Sample[HID].Voltage;
	if(Adc==ADC_Sample[LID].ADC)return ADC_Sample[LID].Voltage;
	
	DAdc=ADC_Sample[HID].ADC-ADC_Sample[LID].ADC;
	DV=ADC_Sample[HID].Voltage-ADC_Sample[LID].Voltage;
	
	M=(float)DV/DAdc;
	
	A=Adc-ADC_Sample[LID].ADC;
	B=ADC_Sample[LID].Voltage;
	
	Out=(A*M)+B;
	
	return Out;
}

uint16_t Get_ADC_Sample(void)
{
	int i;
	uint32_t sum=0;
	uint16_t adc;
	
	HAL_ADC_Start(&hadc1);
	
	for(i=0;i<200;i++)
	{
		HAL_ADC_PollForConversion(&hadc1,0xffffff);
		adc=HAL_ADC_GetValue(&hadc1);
		sum+=adc;
	}
	
	HAL_ADC_Stop(&hadc1);

	return ((float)sum/200.0);
}
